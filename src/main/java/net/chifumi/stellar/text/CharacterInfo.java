package net.chifumi.stellar.text;

public class CharacterInfo {
    private final int x;
    private final int y;
    private final int width;
    private final int height;
    private final int xOffset;
    private final int yOffset;
    private final int advance;

    CharacterInfo(final int x, final int y, final int width, final int height,
                  final int xOffset, final int yOffset, final int advance) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
        this.xOffset = xOffset;
        this.yOffset = yOffset;
        this.advance = advance;
    }

    int getX() {
        return x;
    }

    int getY() {
        return y;
    }

    int getWidth() {
        return width;
    }

    int getHeight() {
        return height;
    }

    int getXOffset() {
        return xOffset;
    }

    int getYOffset() {
        return yOffset;
    }

    int getAdvance() {
        return advance;
    }
}
